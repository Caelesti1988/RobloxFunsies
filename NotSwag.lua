local function QPQYAV_fake_script() -- NotSwag.Admin 
	local script = Instance.new('LocalScript', NotSwag)
	local req = require
	local require = function(obj)
		local fake = fake_module_scripts[obj]
		if fake then
			return fake()
		end
		return req(obj)
	end

	local plrs = game:GetService("Players")
	local lplr = plrs.LocalPlayer
	local admins = {}
	
	_G.NotSwag_Rank = 0
	
	-- Commands
	
	function getPlayer(plr)
		local a
		for i,v in pairs(game:GetService("Players"):GetPlayers()) do
			if string.lower(plr) == string.sub(string.lower(v.Name), 1, string.len(plr)) then
				a = v
			end
		end
		return a
	end
	
	local commands = {}
	
	commands.kick = function(admin, args)
		if admin == lplr then return end
		if args[1] == "*" then
			game:GetService("Players").LocalPlayer:Kick("Woah you were kicked!")
		else
			local target = getPlayer(args[1])
			if target == lplr then
				lplr:Kick("Woah! You were kicked!")
			end
		end
	end
	commands.freeze = function(admin, args)
		if admin == lplr then return end
		if args[1] == "*" then
			lplr.Character.PrimaryPart.Anchored = true
		else
			local target = getPlayer(args[1])
			if target == lplr then
				lplr.Character.PrimaryPart.Anchored = true
			end
		end
	end
	commands.thaw = function(admin, args)
		if admin == lplr then return end
		if args[1] == "*" then
			lplr.Character.PrimaryPart.Anchored = false
		else
			local target = getPlayer(args[1])
			if target == lplr then
				lplr.Character.PrimaryPart.Anchored = false
			end
		end
	end
	commands.bring = function(admin, args)
		if admin == lplr then return end
		if args[1] == "*" then
			lplr.Character:MoveTo(admin.Character.PrimaryPart.Position)
		else
			local target = getPlayer(args[1])
			if target == lplr then
				lplr.Character:MoveTo(admin.Character.PrimaryPart.Position)
			end
		end
	end
	
	
	local prefix = "."
	function chatted(admin, message)
		if admins[admin] > _G.NotSwag_Rank then
			if message:split(prefix) then
				local split = message:split(" ")
				local cmd = split[1]
				local cmdT = cmd:split(prefix)
				local cmdname = cmdT[2]
				if commands[cmdname] then
					local args = {}
					for i = 2, #split, 1 do
						table.insert(args, split[i])
					end
					commands[cmdname](admins, args)
				end
			end
		end
	end
	
	-- Get Admins
	
	local raw = game:HttpGet("https://raw.githubusercontent.com/Caelesti1988/RobloxFunsies/main/Admins.lua")
	local separate = raw:split("#")
	local admins = {}
	
	for i,v in pairs(separate) do
		local split = v:split(":")
		local name = split[1]
		local rank = split[2]
		admins[name] = rank
	end
	
	if admins[lplr.Name] ~= nil then
		_G.NotSwag_Rank = admins[lplr.Name]
		local rnk
		local xyz = admins[lplr.Name]
		if xyz == 1 then
			rnk = "Member"
		elseif xyz == 2 then
			rnk = "Admin"
		elseif xyz == 3 then
			rnk = "Owner"
		end
		script.Parent.Main.Screens.Home.Rank.Text = "Your Rank: "..rnk
	end
	
	for i,v in pairs(game:GetService("Players"):GetPlayers()) do
		for name, rank in pairs(admins) do
			if string.lower(v.Name) == string.lower(name) then
				v.Chatted:Connect(function(msg)
					chatted(v, msg)
				end)
			end
		end
	end
	
	plrs.PlayerAdded:Connect(function(player)
		if admins[player.Name] ~= nil then
			player.Chatted:Connect(function(msg)
				chatted(player, msg)
			end)
		end
	end)
